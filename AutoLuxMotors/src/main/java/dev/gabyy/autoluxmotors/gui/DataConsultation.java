    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(carTable.getRowCount() > 0){
            if(carTable.getSelectedRow() != -1){
                int idCar = Integer.parseInt(String.valueOf(carTable.getValueAt(carTable.getSelectedRow(), 0)));
                control.deleteCar(idCar);
                createTable();
                showMessage("¡Success!", "Inf", "Dealership says: ");
            }else {
                showMessage("Select a Car", "Error", "Dealership says: ");
            }
        }else {
            showMessage("Table is Empty","Error","Dealership says: ");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(carTable.getRowCount() > 0){
            if(carTable.getSelectedRow() != -1){
                int idCar = Integer.parseInt(String.valueOf(carTable.getValueAt(carTable.getSelectedRow(), 0)));
                UpdateCarGUI updateCar = new UpdateCarGUI(idCar);
                updateCar.setVisible(true);
                updateCar.setLocationRelativeTo(null);
                this.dispose();
                createTable();
                showMessage("¡Success!", "Inf", "Dealership says: ");
            }else {
                showMessage("Select a Car", "Error", "Dealership says: ");
            }
        }else {
            showMessage("Table is Empty","Error","Dealership says: ");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed
    
    public void showMessage(String msg, String type, String title) {
        JOptionPane optionPane = new JOptionPane("Paw says: " + msg);
        if (type.equals("Inf")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (type.equals("Error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(title);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
    
