    private void createTable() {
        DefaultTableModel modelTable = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        
        String title[] = {"Id", "Model", "Brand", "Enginge", "Color", "Plate", "Number of Door"};
        modelTable.setColumnIdentifiers(title);
        carTable.setModel(modelTable);
        
        List<Car> listCar = control.bringCar();
        
        if(listCar != null){
            for(Car car : listCar){
                Object object[] = {
                    car.getId(),car.getModel(),car.getBrand(),
                    car.getEngine(),car.getColor(), car.getPatent(), car.getDoorAMount()};
                modelTable.addRow(object);
            }
        }
        carTable.setModel(modelTable);
    }
