    private void loadTable() {
        DefaultTableModel tableModel = new DefaultTableModel() {
            // Disabled rows and columns 

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        // Set the columns names
        String titles[] = {"Owner Name", "Owner Number",
            "Pet Name", "Pet breed", "Pet Color", "isAllergic?",
            "Special Attention", "Observations"};
        tableModel.setColumnIdentifiers(titles);

        // Load data
        List<Pet> petList = control.bringPet();

        if (petList != null) {
            for (Pet pet : petList) {
                Object[] obj = { pet.getNumClient(),
                    pet.getOwner().getName(), pet.getOwner().getCelOwner(),
                    pet.getName(), pet.getBreed(), pet.getColor(), pet.getAllergic(),
                    pet.getSpecialAttention(), pet.getObservations()
                };
                tableModel.addRow(obj);
            }
        }

        //Assign tableModel to Table variable. That is, setModel.
        petTable.setModel(tableModel);
    }
