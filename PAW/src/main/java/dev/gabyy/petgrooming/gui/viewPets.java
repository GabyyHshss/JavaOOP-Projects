    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        if (petTable.getRowCount() > 0) {
            if (petTable.getSelectedRow() != -1) {
                int clientId = Integer.parseInt(String.valueOf(petTable.getValueAt(petTable.getSelectedRow(), 0)));
                control.deletePet(clientId);
                // SHOW MSG
                showMessage("Data was successfuly deleted", "Inf", "Pet was deleted");
                loadTable();
            } else {
                showMessage("Data was not selected", "Error", "You should select a pet");
            }
        } else {
            showMessage("Table is empty", "Error", "Add data if you wanna use this option");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed
    
    public void showMessage(String msg, String type, String title) {
        JOptionPane optionPane = new JOptionPane("Paw says: " + msg);
        if (type.equals("Inf")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (type.equals("Error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(title);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
    
    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        if (petTable.getRowCount() > 0) {
            if (petTable.getSelectedRow() != -1) {
                int clientId = Integer.parseInt(String.valueOf(petTable.getValueAt(petTable.getSelectedRow(), 0)));
                ModifyPet modifyScreen = new ModifyPet(clientId);
                modifyScreen.setVisible(true);
                modifyScreen.setLocationRelativeTo(null);
            } else {
                showMessage("Data was not selected", "Error", "You should select a pet");
            }
        } else {
            showMessage("Table is empty", "Error", "Add data if you wanna use this option");
        }
    }//GEN-LAST:event_updateBtnActionPerformed

